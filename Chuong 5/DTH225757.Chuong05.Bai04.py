# Một số hàm quan trọng trong xử lý chuỗi của Python:
# 1. len() - Trả về độ dài của chuỗi.
# 2. str() - Chuyển đổi giá trị thành chuỗi.
# 3. lower() - Chuyển đổi tất cả các ký tự trong chuỗi thành chữ thường.
# 4. upper() - Chuyển đổi tất cả các ký tự trong chuỗi thành chữ hoa.
# 5. split() - Tách chuỗi thành danh sách các chuỗi con dựa trên ký tự phân cách.
# 6. join() - Nối các phần tử của danh sách thành một chuỗi, với ký tự phân cách.
# 7. replace() - Thay thế một chuỗi con bằng một chuỗi con khác trong chuỗi.
# 8. find() - Tìm vị trí đầu tiên của chuỗi con trong chuỗi.
# 9. strip() - Loại bỏ các ký tự trắng ở đầu và cuối chuỗi.
# 10. format() - Định dạng chuỗi bằng cách chèn các giá trị vào chuỗi.